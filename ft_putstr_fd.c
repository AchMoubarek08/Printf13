#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (s == NULL)
		write(1, "(null)", 6);
	else if(s)
	{
		int	i;
		i = 0;
		while (s[i])
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
