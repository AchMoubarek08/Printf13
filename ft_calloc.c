#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*s;

	s = malloc(nmemb * size);
	if (s == NULL)
		return (0);
	ft_memset (s, 0, nmemb * size);
	return (s);
}
